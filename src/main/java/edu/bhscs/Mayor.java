package edu.bhscs;

public class Mayor {
  // PROPERTIES AND FIELDS
  Player user;
  String name;
  int power;
  baker b;


  // CONSTRUCTOR
  public Mayor() {
    this.name = "The Mayor";
  }

  public Mayor(String name) {
    this.name = name;
  }

  public Mayor(Player user, String name, int power) {
    this.user = user;
    this.power = power;
  }

  // METHODS
  public boolean approvedStore(Store store, baker b) {

    if (b.getSkill() > 5) return true;
    return false;
  }
}
